friendsList.forEach(element => {		//For each loop to traverse the List
	const friendsCard = document.createElement('div');		
    friendsCard.setAttribute('class','card');		//Card containing the picture and the details

    let friendsProfile = document.createElement('img'); //Setting a profile picture for the friends
    friendsProfile.setAttribute('src', element.img);
    friendsProfile.setAttribute('alt', 'profile picture');  
    friendsProfile.setAttribute('class','profile');
    friendsCard.append(friendsProfile);

    let friendsDetails = document.createElement('div');	//Details of the friends including their name and email
    friendsDetails.setAttribute('class','friends-details')
    let fullName = document.createElement('p');
    fullName.setAttribute('class','name');
    fullName.textContent = element.first_name+ ' ' + element.last_name;
    friendsDetails.append(fullName);

    let friendsEmail = document.createElement('a');  //Setting up the email
    friendsEmail.setAttribute('class', 'friendsEmail');  
    friendsEmail.setAttribute('href', '#');
    friendsEmail.textContent = element.friendsEmail;
    friendsDetails.append(friendsEmail); //Appending the mail to the details
    friendsCard.append(friendsDetails);	//Appending the Details to the card
    documentFragment.append(friendsCard);		//Appending the card to the main body
});
